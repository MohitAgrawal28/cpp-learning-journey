#include<iostream>
using namespace std;
int main () {
    int x = 20;
    cout<<x<<endl;
    x++; //This is known as Post Increment. It means X is incremented by 1 
    cout<<x<<endl;
    x--; //This is known as Post Decrement. It means X is Decremented by 1 
    cout<<x<<endl;
    ++x; //This is known as Pre Increment. It means X is incremented by 1 
    cout<<x<<endl;
    --x; //This is known as Pre Decrement. It means X is Decremented by 1 
    cout<<x<<endl;
}